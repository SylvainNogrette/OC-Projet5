def sum(a, b):
    """ Return the sum of two numbers

    :param a: int or float
    :param b: int or float
    :return: int or float
    """
    return a + b
 
def subtraction(a, b):
    """ Return the subtraction of two numbers

    :param a: int or float
    :param b: int or float
    :return: int or float
    """
    return a - b
